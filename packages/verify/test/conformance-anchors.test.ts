/**
 * Spec §2.11 anchor-plurality conformance (D138; extends D050).
 *
 * Loads each case from spec/conformance/2.11/anchors/cases/ and asserts the
 * expected invariants against the LIBRARY implementations:
 *
 *   - verifier side: `verifyAnchorPlurality` from
 *     packages/verify/src/anchor-plurality.ts — legacy `log_proofs`
 *     elements (absent anchor_type) parse as `atrib-log`; discriminator
 *     malformation rules; unknown anchor_type values surfaced, not counted,
 *     never invalidating; the `anchor_plurality` annotation (single_anchor
 *     is a tier, never a failure; pending OTS proofs excluded until
 *     upgraded in place; independence counted over operator groups); and
 *     the unchanged §2.11.4 hard conditions (cross_log_threshold_not_met,
 *     cross_log_equivocation_detected, cross_log_censorship_suspected).
 *   - producer side: `resolveAnchorPosture` from
 *     packages/mcp/src/anchors.ts — the §2.11.12 allow_single_anchor
 *     posture resolution.
 *   - the fresh anchoring-signature claim artifact
 *     ("atrib-anchor/v1:" + record_hash, Ed25519-signed by an anchoring key
 *     that is NOT the record's signing key) and the proof that the record's
 *     own signature cannot ride the digest path.
 *   - determinism: two runs over the same bundle + trust config produce an
 *     identical verdict (the §4.6-style two-run discipline).
 *
 * The corpus is regenerated by
 * packages/log-dev/scripts/generate-conformance-anchors.ts; this test
 * consumes the committed vectors only.
 */

