const CORPUS = join(__dirname, '../../../spec/conformance/mcp-extension/cases')

const EXT_ID = 'dev.atrib/attribution'
const CLIENT_CAPABILITIES_META_KEY = 'io.modelcontextprotocol/clientCapabilities'
const RESERVED_PREFIX_LABELS = new Set(['mcp', 'modelcontextprotocol'])
const KNOWN_SETTINGS_FIELDS = new Set([
  'version',
  'accept',
  'signs',
  'receipts',
  'disclosure',
  'creator_key',
  'logs',
  'directory',
])
const KNOWN_ACCEPT_VALUES = new Set(['token', 'record'])
const LOG_SUBMISSION_STATUSES = new Set(['queued', 'submitted', 'disabled', 'failed'])
const HEX32 = /^[0-9a-f]{32}$/

interface CaseFile {
  name: string
  spec_section: string
  extension: string
  description: string
  input: Record<string, unknown>
  expected: Record<string, unknown>
}

function loadCase(name: string): CaseFile {
  return JSON.parse(readFileSync(join(CORPUS, `${name}.json`), 'utf8')) as CaseFile
}

function jcsSha256Hex(value: unknown): string {
  const json = canonicalize(value)
  if (json === undefined) throw new Error('canonicalization produced undefined')
  return hexEncode(sha256(new TextEncoder().encode(json)))
}

function isPlainObject(value: unknown): value is Record<string, unknown> {
  return typeof value === 'object' && value !== null && !Array.isArray(value)
}

// ─── Reference implementation: extension spec §3 (identifier grammar) ──

function validateExtensionIdentifier(identifier: string): boolean {
  const parts = identifier.split('/')
  if (parts.length !== 2) return false
  const [prefix, name] = parts
  if (!prefix || !name) return false
  const labels = prefix.split('.')
  if (labels.length < 2) return false
  for (const label of labels) {
    if (!/^[a-z0-9-]+$/.test(label)) return false
    if (RESERVED_PREFIX_LABELS.has(label)) return false
  }
  return /^[a-z0-9-]+$/.test(name)
}

// ─── Reference implementation: extension spec §4 (settings validation) ─

interface SettingsValidation {
  declared: boolean
  negotiatedVersion?: string
  effectiveAccept: string[]
  ignoredSettingsFields: string[]
  ignoredAcceptValues: string[]
}

function validateSettings(settings: unknown): SettingsValidation {
  const undeclared: SettingsValidation = {
    declared: false,
    effectiveAccept: ['token'],
    ignoredSettingsFields: [],
    ignoredAcceptValues: [],
  }
  if (!isPlainObject(settings)) return undeclared
  const version = settings.version
  if (typeof version !== 'string' || version.length === 0) return undeclared

  const ignoredSettingsFields = Object.keys(settings)
    .filter((k) => !KNOWN_SETTINGS_FIELDS.has(k))
    .sort()

  const ignoredAcceptValues: string[] = []
  let effectiveAccept: string[] = ['token']
  if (Array.isArray(settings.accept)) {
    const recognized = settings.accept.filter((v): v is string => {
      if (typeof v === 'string' && KNOWN_ACCEPT_VALUES.has(v)) return true
      if (typeof v === 'string') ignoredAcceptValues.push(v)
      return false
    })
    if (recognized.length > 0) effectiveAccept = recognized
  }

  return {
    declared: true,
    negotiatedVersion: version,
    effectiveAccept,
    ignoredSettingsFields,
    ignoredAcceptValues,
  }
}

/** Extension spec §6.1: per-request client declaration detection. */
function detectClientDeclaration(meta: unknown): SettingsValidation {
  if (!isPlainObject(meta)) return validateSettings(undefined)
  const caps = meta[CLIENT_CAPABILITIES_META_KEY]
  if (!isPlainObject(caps)) return validateSettings(undefined)
  const extensions = caps.extensions
  if (!isPlainObject(extensions)) return validateSettings(undefined)
  return validateSettings(extensions[EXT_ID])
}

// ─── Reference implementation: extension spec §5.2 (Ladder 1) ──────────

type TokenSource = 'extension' | 'meta-atrib' | 'tracestate' | 'x-atrib-chain' | null

interface TokenResolution {
  source: TokenSource
  recordHashHex?: string
  creatorKey?: string
  conflictWarning: boolean
}

function decodeCarrier(value: unknown): { hashHex: string; key: string } | null {
  if (typeof value !== 'string' || value.length === 0) return null
  const decoded = decodeToken(value)
  if (!decoded) return null
  return { hashHex: hexEncode(decoded.recordHash), key: base64urlEncode(decoded.creatorKey) }
}

function resolveInboundToken(meta: unknown): TokenResolution {
  const m = isPlainObject(meta) ? meta : {}

  // Legacy rungs, in §1.5.4 order, via the production parsers.
  const legacy: { source: Exclude<TokenSource, 'extension' | null>; hit: { hashHex: string; key: string } }[] = []
  const metaAtrib = decodeCarrier(m.atrib)
  if (metaAtrib) legacy.push({ source: 'meta-atrib', hit: metaAtrib })
  if (typeof m.tracestate === 'string') {
    const entry = parseTracestateAtrib(m.tracestate)
    const decoded = entry ? decodeCarrier(entry) : null
    if (decoded) legacy.push({ source: 'tracestate', hit: decoded })
  }
  const xChain = decodeCarrier(m['X-Atrib-Chain'] ?? m['x-atrib-chain'])
  if (xChain) legacy.push({ source: 'x-atrib-chain', hit: xChain })

  // Extension rung on top: lenient parse, falls through when malformed.
  const block = m[EXT_ID]
  if (isPlainObject(block)) {
    const extension = decodeCarrier(block.token)
    if (extension) {
      const conflictWarning = legacy.some((l) => l.hit.hashHex !== extension.hashHex)
      return {
        source: 'extension',
        recordHashHex: extension.hashHex,
        creatorKey: extension.key,
        conflictWarning,
      }
    }
  }

  const first = legacy[0]
  if (first) {
    return {
      source: first.source,
      recordHashHex: first.hit.hashHex,
      creatorKey: first.hit.key,
      conflictWarning: false,
    }
  }
  return { source: null, conflictWarning: false }
}

// ─── Reference implementation: extension spec §5.3 (Ladder 2) ──────────

type ContextSource = 'argument' | 'extension' | 'traceparent' | 'env-registry-fallthrough'

interface ContextResolution {
  contextId?: string
  source: ContextSource
  mismatchWarning: boolean
}

function resolveContextIdentity(explicitArgument: unknown, meta: unknown): ContextResolution {
  const m = isPlainObject(meta) ? meta : {}
  const block = m[EXT_ID]
  const extensionValue =
    isPlainObject(block) && typeof block.context_id === 'string' && HEX32.test(block.context_id)
      ? block.context_id
      : undefined
  const traceValue = typeof m.traceparent === 'string' ? extractTraceId(m.traceparent) : undefined

  if (typeof explicitArgument === 'string' && HEX32.test(explicitArgument)) {
    const lowerCarrier = extensionValue ?? traceValue
    return {
      contextId: explicitArgument,
      source: 'argument',
      mismatchWarning: lowerCarrier !== undefined && lowerCarrier !== explicitArgument,
    }
  }
  if (extensionValue !== undefined) {
    return {
      contextId: extensionValue,
      source: 'extension',
      mismatchWarning: traceValue !== undefined && traceValue !== extensionValue,
    }
  }
  if (traceValue !== undefined) {
    return { contextId: traceValue, source: 'traceparent', mismatchWarning: false }
  }
  // No per-request carrier resolved. Producer-side D078/D083 env-file
  // resolution applies next; at the transport layer the result is undefined.
  return { source: 'env-registry-fallthrough', mismatchWarning: false }
}

// ─── Reference implementation: extension spec §6 (gated result carriage) ─

function buildExtensionResultBlock(record: AtribRecord, includeRecord: boolean): Record<string, unknown> {
  const shortName = Object.entries(EVENT_TYPE_SHORT_TO_URI).find(([, uri]) => uri === record.event_type)?.[0]
  const block: Record<string, unknown> = {
    token: encodeToken(record),
    receipt: {
      record_hash: `sha256:${hexEncode(sha256(canonicalRecord(record)))}`,
      creator_key: record.creator_key,
      context_id: record.context_id,
      event_type: shortName ?? record.event_type,
      chain_root: record.chain_root,
      log_submission: 'queued',
    },
  }
  if (includeRecord) block.record = record
  return block
}

/**
 * §5.8-conformant result extension. Signing, capability detection, and
 * carriage all live inside one try/catch: any failure leaves the tool
 * result byte-identical to passthrough and never throws to the caller.
 */
function extendResultSafely(
  result: Record<string, unknown>,
  requestMeta: unknown,
  sign: () => AtribRecord,
): void {
  try {
    const record = sign()
    const declaration = detectClientDeclaration(requestMeta)
    writeOutboundContext(result, record)
    if (declaration.declared) {
      const meta = result._meta as Record<string, unknown>
      meta[EXT_ID] = buildExtensionResultBlock(record, declaration.effectiveAccept.includes('record'))
    }
  } catch {
    // §5.8: extension failures never affect the primary tool result.
  }
}

// ─── Reference implementation: extension spec §6.2 (receipt verification) ─

interface ReceiptBlock {
  token: string
  receipt: {
    record_hash: string
    creator_key: string
    context_id: string
    event_type: string
    chain_root: string
    log_submission: string
  }
  record?: AtribRecord
}

function verifyReceiptBlock(block: ReceiptBlock): { valid: boolean; mismatched: string[] } {
  const mismatched: string[] = []
  const decoded = decodeToken(block.token)
  if (!decoded) return { valid: false, mismatched: ['token'] }
  const tokenHashHex = hexEncode(decoded.recordHash)
  const tokenKey = base64urlEncode(decoded.creatorKey)

  if (block.receipt.record_hash !== `sha256:${tokenHashHex}`) mismatched.push('record_hash')
  if (block.receipt.creator_key !== tokenKey) mismatched.push('creator_key')
  if (!LOG_SUBMISSION_STATUSES.has(block.receipt.log_submission)) mismatched.push('log_submission')

  if (block.record) {
    const recordHashHex = hexEncode(sha256(canonicalRecord(block.record)))
    if (block.token !== encodeToken(block.record) && !mismatched.includes('token')) {
      mismatched.push('token')
    }
    if (block.receipt.record_hash !== `sha256:${recordHashHex}` && !mismatched.includes('record_hash')) {
      mismatched.push('record_hash')
    }
    if (block.receipt.creator_key !== block.record.creator_key && !mismatched.includes('creator_key')) {
      mismatched.push('creator_key')
    }
    if (block.receipt.context_id !== block.record.context_id) mismatched.push('context_id')
    if (block.receipt.chain_root !== block.record.chain_root) mismatched.push('chain_root')
  }

  return { valid: mismatched.length === 0, mismatched }
}

