// ═══ Family 2: gating ═══════════════════════════════════════════════════

function runGatedResult(c: CaseFile): Record<string, unknown> {
  const record = c.input.signed_record as AtribRecord
  const result: Record<string, unknown> = { content: [{ type: 'text', text: 'ok' }] }
  extendResultSafely(result, c.input.request_meta, () => record)
  return result
}

describe('mcp-extension conformance: gating', () => {
  it('declared-receipt-present: prefixed block alongside byte-identical legacy keys', async () => {
    const c = loadCase('gating--declared-receipt-present')
    const expected = c.expected as {
      extension_block_present: boolean
      result_meta: Record<string, unknown>
      legacy_keys: Record<string, string>
    }
    const result = runGatedResult(c)
    const meta = result._meta as Record<string, unknown>
    expect(EXT_ID in meta).toBe(expected.extension_block_present)
    expect(meta).toEqual(expected.result_meta)
    expect(meta.atrib).toBe(expected.legacy_keys.atrib)
    expect(meta.tracestate).toBe(expected.legacy_keys.tracestate)
    expect(meta['X-Atrib-Chain']).toBe(expected.legacy_keys['X-Atrib-Chain'])

    // The attached record body is a real signed record (Tier-3 check).
    const block = meta[EXT_ID] as ReceiptBlock
    expect(block.record).toBeDefined()
    expect(await verifyRecordSignature(block.record as AtribRecord)).toBe(true)
  })

  it('declared-token-only: receipt present, record body omitted', () => {
    const c = loadCase('gating--declared-token-only')
    const expected = c.expected as {
      extension_block_present: boolean
      record_body_present: boolean
      result_meta: Record<string, unknown>
    }
    const result = runGatedResult(c)
    const meta = result._meta as Record<string, unknown>
    expect(EXT_ID in meta).toBe(expected.extension_block_present)
    const block = meta[EXT_ID] as Record<string, unknown>
    expect('record' in block).toBe(expected.record_body_present)
    expect(meta).toEqual(expected.result_meta)
  })

  it('undeclared-legacy-only: byte-identical to pre-extension writeOutboundContext output', () => {
    const c = loadCase('gating--undeclared-legacy-only')
    const expected = c.expected as {
      extension_block_present: boolean
      result_meta: Record<string, unknown>
      result_meta_jcs_sha256_hex: string
    }
    const result = runGatedResult(c)
    const meta = result._meta as Record<string, unknown>
    expect(EXT_ID in meta).toBe(expected.extension_block_present)
    expect(meta).toEqual(expected.result_meta)
    // Byte-identity pinned by JCS hash against real writeOutboundContext output.
    expect(jcsSha256Hex(meta)).toBe(expected.result_meta_jcs_sha256_hex)
  })

  it('malformed-clientcapabilities-undeclared: treated as undeclared, no error', () => {
    const c = loadCase('gating--malformed-clientcapabilities-undeclared')
    const expected = c.expected as {
      extension_block_present: boolean
      error_raised: boolean
      result_meta: Record<string, unknown>
    }
    let raised = false
    let result: Record<string, unknown> = {}
    try {
      result = runGatedResult(c)
    } catch {
      raised = true
    }
    expect(raised).toBe(expected.error_raised)
    const meta = result._meta as Record<string, unknown>
    expect(EXT_ID in meta).toBe(expected.extension_block_present)
    expect(meta).toEqual(expected.result_meta)
  })
})

