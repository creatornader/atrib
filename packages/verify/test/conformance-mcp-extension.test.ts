/**
 * dev.atrib/attribution MCP-extension conformance (P049 / spec §1.5.4).
 *
 * Loads each case from spec/conformance/mcp-extension/cases/ (the committed
 * vectors, never the generator) and asserts the expected invariants against
 * a reference implementation of the extension contract defined in
 * docs/extensions/dev.atrib-attribution/v0.1.md:
 *
 *   - capability settings validation + the SEP-2133 reserved-prefix rule
 *   - receipt opt-in gating with byte-identical legacy fallback
 *   - Ladder 1 (inbound propagation token) precedence
 *   - Ladder 2 (context identity) precedence
 *   - receipt integrity against real Ed25519-signed records
 *   - the §5.8 degradation contract under forced failures
 *
 * The reference resolution reuses the same primitives as
 * packages/mcp/src/context.ts (decodeToken, parseTracestateAtrib,
 * extractTraceId, writeOutboundContext) so the legacy rungs are exercised
 * through the production implementation, with the extension rung layered
 * on top exactly as §1.5.4 specifies. The corpus is regenerated by
 * packages/log-dev/scripts/generate-conformance-mcp-extension.ts.
 */

